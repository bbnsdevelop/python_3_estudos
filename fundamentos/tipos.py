print('Type: ', type(True), 'Value: ', True)
print('Type: ', type(False), 'Value: ', False)
print('Type: ', type(1.2 + 1), 'Value: ', 1.2 + 1)
print('Type: ', type('olá, mundo'), 'Value: ', 'Olá, mundo')
print('Type: ', type("Hello, World"), 'Value: ', "Hello, world")
print('Type: ', type(['olá, mundo', 1, 2, 3]), 'Value: ', ['Olá, mundo', 1, 3])
print('Type: ', type({'frase': 'olá, mundo'}), 'Value: ', {'frase': 'olá, mundo'})
print('Type: ', type(None), 'Value: ', None)
